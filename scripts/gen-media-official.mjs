// Generate src/media-official.ts from website/data/media-models.json.
//
// The JSON is the human-edited cross-provider price table; its "official" route
// per model is the model-maker's first-party list price — the savings baseline
// for media routing (see src/media.ts). This script lifts those official rows
// into a bundled, importable map so the runtime can baseline ANY registry
// (incl. downstream ones like ai-art's) by modelId, with no JSON import at
// runtime. Re-run after editing media-models.json.
//
//   node scripts/gen-media-official.mjs
import { readFileSync, writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, resolve } from "node:path";

const here = dirname(fileURLToPath(import.meta.url));
const SRC = resolve(here, "../website/data/media-models.json");
const OUT = resolve(here, "../src/media-official.ts");

const j = JSON.parse(readFileSync(SRC, "utf8"));
const entries = [];
for (const m of j.models) {
  const o = m.routes.find((r) => r.provider === "official");
  if (!o) continue; // open-weight / no first-party price → no baseline (shown as "—")
  entries.push([m.id, { unit: o.unit, cents: o.cents }]);
}
entries.sort(([a], [b]) => a.localeCompare(b));

const body = entries
  .map(([id, p]) => `  ${JSON.stringify(id)}: { unit: ${JSON.stringify(p.unit)}, cents: ${p.cents} },`)
  .join("\n");

const ts = `// GENERATED by scripts/gen-media-official.mjs from website/data/media-models.json — DO NOT EDIT.
// Model-maker first-party list prices (the "go direct" cost) keyed by logical
// modelId, in the model's native unit. Used as the media savings baseline: a
// route's savings is officialPrice − actualCost (see ./media). Models with no
// first-party API price (open weights served only by aggregators) are absent —
// those get no baseline, so the dashboard shows "—" rather than a fake 0%.
import type { MediaPricing } from "./media";

export const OFFICIAL_PRICES: Record<string, MediaPricing> = {
${body}
};
`;

writeFileSync(OUT, ts);
console.log(`wrote ${OUT} — ${entries.length} official prices`);
